#Conversion a entero
numero_cadena = "42"
numero_entero = int(numero_cadena)
print(numero_entero)
print(type(numero_entero))

# Conversión a flotante
numero_entero = 42
numero_flotante = float(numero_entero)
print(numero_flotante)
print(type(numero_flotante))

#Conversión a cadena
numero_entero = 42
cadena = str(numero_entero)
print(cadena)
print(type(cadena))
